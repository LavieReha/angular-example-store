import { Component } from '@angular/core';
// Imports the data from products object defined in products.ts.
import { products } from '../products';

@Component({
  selector: 'app-product-list',
  templateUrl: './product-list.component.html',
  styleUrls: ['./product-list.component.css']
})
export class ProductListComponent {
  products = products;
  // Define the behavior that happens when the user clicks one of the buttons.
  // The parent, ProductListComponent — not the ProductAlertsComponent — acts when the child raises the event.
  share() {
    window.alert('The product has been shared!');
  }

  onNotify() {
    window.alert('You will be notified when the product goes on sale');
  }
}


